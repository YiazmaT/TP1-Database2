    public void erro(int e){
        switch(e){
            case 1: JOptionPane.showMessageDialog(null, "Quantidade Inválida!!"); break;
            case 2: JOptionPane.showMessageDialog(null, "Código do produto Inválido!!"); break;
            case 3: JOptionPane.showMessageDialog(null, "Código do produto Inexistente!!"); break;
            case 4: JOptionPane.showMessageDialog(null, "Produto já existente na nota!!"); break;
        }
    }
    
    public void listarTabelaProdutos(ArrayList<Produto> p){
        String[] s = new String[1];
        DefaultTableModel dtm = (DefaultTableModel) tableProdutos.getModel();
        dtm.setRowCount(0);
        for(Produto atual : p){
            s[0] = atual.getNome();
            dtm.addRow(s);
        }
    }
    
    public void listarTabelaComprados(ArrayList<Produto> p){
        DefaultTableModel dtm = (DefaultTableModel) produtosNota.getModel();
        dtm.setRowCount(0);
        String[] linha = new String [5];
        totalNota =0.0f;
        for(Produto atual : p){
            linha[0] = String.valueOf(atual.getId_produto());
            linha[1] = atual.getNome();
            linha[2] = String.valueOf(atual.getQuantidade());
            linha[3] = String.format("%.2f",atual.getValor());
            linha[4] = String.format("%.2f", atual.getValor()*atual.getQuantidade());
            totalNota += atual.getValor()*atual.getQuantidade();
            dtm.addRow(linha);
        }
        totalField.setText("R$"+String.format("%.2f", totalNota));
        if(p.isEmpty()){
            pagamentoButton.setEnabled(false);
            return;
        }
        pagamentoButton.setEnabled(true);
    }

