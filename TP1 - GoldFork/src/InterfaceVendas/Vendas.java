    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //pegar e validar quantidade;
        String q = quantidadeField.getText();
        if(q.equals("")) {erro(1); return;}
        if(!q.matches("[0-9]{1,50}")){this.erro(1); return;}
        int quantidade = Integer.parseInt(q);
        if(quantidade <= 0) {this.erro(1); return;}
        
        //pegar e validar codigo produto;
        String c = codigoField.getText();
        if(c.equals("")) {erro(2); return;}
        if(!c.matches("[0-9]{1,50}")){this.erro(2); return;}
        int codigo = Integer.parseInt(c);
        if(codigo <= 0) {this.erro(2); return;}
        
        //veririfcar se o produto já está na nota;
        for(Produto atual : this.produtoComprados){
            if(atual.getId_produto() == codigo){
                this.erro(4);
                return;
            }
        }
        
        //verificar se o código existe
        Produto seraAdicionado = null;
        for(Produto atual : this.produtos){
            if(atual.getId_produto() == codigo){
                seraAdicionado = atual;
                seraAdicionado.setQuantidade(quantidade);
                break;
            }
        }
        if(seraAdicionado == null){
            this.erro(3);
            return;
        }
        
        boolean possuiEstoque = BancoDeDados.possuiEstoqueParaVender(seraAdicionado, pai.getIdUsuarioLoja());
        if(possuiEstoque == false){
            JOptionPane.showMessageDialog(null, "Estoque insuficiente!!");
            return;
        }
        
        //adicinoar na tabela;
        this.produtoComprados.add(seraAdicionado);
        listarTabelaComprados(this.produtoComprados);
    }//GEN-LAST:event_jButton1ActionPerformed

