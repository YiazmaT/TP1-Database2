    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //pesquisar produtos que possuem a string como chave e addnar no array de pesquisa atual;
        String search = searchField.getText().toUpperCase();
        if(search.equals("")){
            produtosPesquisaAtual=produtos;
            this.listarTabelaProdutos(produtosPesquisaAtual);
            return;
        }
        else{
            produtosPesquisaAtual = new ArrayList<Produto>();
            for(Produto atual : produtos){
                if(atual.getNome().contains(search)){
                    produtosPesquisaAtual.add(atual);
                }
            }
            this.listarTabelaProdutos(produtosPesquisaAtual);
            return;
        }
    }//GEN-LAST:event_jButton4ActionPerformed

