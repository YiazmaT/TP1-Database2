    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //pegar e validar nome;
        String nome = nomeField1.getText();
        if(nome.equals("")) {this.erro(3); return;}
        if(nome.length() > 100) {this.erro(4); return;}
        nome = nome.toLowerCase();
        if(!nome.matches("([0-9a-zA-Z ]|â|à|ã|ä|á|Á|ê|ë|é|É|î|ï|í|Í|ô|õ|ö|ó|Ó|û|ü|ú|Ú|'|ç|-|_){1,50}")){this.erro(1); return;}
        nome = nome.toUpperCase();
        if(!this.produto.getNome().equals(nome)){
            if(BancoDeDados.verificaIfProdutoAtomicoComNomeIgual(nome) == true){ this.erro(6); return;}
        }

        //pegar e validar unidade;
        String unidade = unidadeField1.getText();
        if(unidade.equals("")) {this.erro(3); return;}
        if(unidade.length() > 25) {this.erro(5); return;}
        if(!unidade.matches("([0-9a-zA-Z ]|â|à|ã|ä|á|Á|ê|ë|é|É|î|ï|í|Í|ô|õ|ö|ó|Ó|û|ü|ú|Ú|'|ç|-|_){1,50}")){this.erro(2); return;}

        int confirm;
        confirm = JOptionPane.showConfirmDialog(null, "Deseja Realmente Alterar os Dados do Produto Atômico para: "
                +"\nNome: "+nome
                +"\nUnidade de Medida: "+unidade
                , "Modificar Produto Atômico", JOptionPane.YES_NO_OPTION);
        if(confirm == 0){
            BancoDeDados.updateProdutoAtomico(this.produto.getIdProdutoAtomico(), nome, unidade);
        }
        else return;
        
        this.dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

    public void erro(int e){
        switch(e){
            case 1: JOptionPane.showMessageDialog(null, "Nome Inválido, utilize apenas letras!!"); break;
            case 2: JOptionPane.showMessageDialog(null, "Unidade Inválida, utilize apenas letras ou números!!"); break;
            case 3: JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!!"); break;
            case 4: JOptionPane.showMessageDialog(null, "Nome pode ter no máximo 100 caracteres!!"); break;
            case 5: JOptionPane.showMessageDialog(null, "Unidade pode ter no máximo 25 caracteres!!"); break;
            case 6: JOptionPane.showMessageDialog(null, "Já existe um Produto Atômico cadastrado com esse nome!!"); break;
        }
    }

