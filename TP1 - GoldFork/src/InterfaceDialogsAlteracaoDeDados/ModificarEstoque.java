    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(novaQuantidadeField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor insira uma quantidade!!");
            return;
        }
        float quantidade = Float.parseFloat(novaQuantidadeField.getText().replaceAll("\\,", "\\."));
        if(quantidade < 0){
            JOptionPane.showMessageDialog(null, "Não são permitidas quantidades negativas");
            return;
        }
        
        BancoDeDados.updateEstoque(this.produto.getIdProdutoAtomico(), this.loja.getId_lanchonete(), quantidade);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    public void preecherCampos(){
        nomeLojaTT.setText(this.loja.getNome());
        nomeProdutoTT.setText(this.produto.getNome());
        unidadeProdutoTT.setText(this.produto.getUnidade());
        quantidadeAtualTT.setText(String.valueOf(this.produto.getQuantidade()));
    }
    
