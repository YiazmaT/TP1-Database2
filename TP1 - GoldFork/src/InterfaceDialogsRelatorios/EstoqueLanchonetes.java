    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int linha = estoqueTable.getSelectedRow();
        if(linha < 0) return;
        
        //pegar loja ao qual o item pertence;
        for(Loja atual : lojas){
            if(atual.getId_lanchonete() == indiceIdLoja.get(linha)){
                ModificarEstoque m = new ModificarEstoque(this.pai, true, produtosTabela.get(linha), atual);
                m.setLocationRelativeTo(this.pai);
                m.setVisible(true);
                
                this.preencherTabela();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    public void preencherTabela(){
        DefaultTableModel dtm = (DefaultTableModel) estoqueTable.getModel();
        dtm.setRowCount(0);
        
        produtosTabela = new ArrayList<ProdutoAtomico>();
        indiceIdLoja = new ArrayList<Integer>();
        
        ArrayList<ProdutoAtomico> p;
        String [] linha = new String[4];
        for(Loja lojaAtual: lojas){
            p = BancoDeDados.estoqueDeUmaLoja(lojaAtual.getId_lanchonete());
            if(!p.isEmpty()){
                for(ProdutoAtomico pas : p){
                    this.indiceIdLoja.add(lojaAtual.getId_lanchonete());
                    this.produtosTabela.add(pas);
                    linha[0] = pas.getNome();
                    linha[1] = pas.getUnidade();
                    linha[2] = String.valueOf(pas.getQuantidade());
                    linha[3] = lojaAtual.getNome();
                    dtm.addRow(linha);
                }
            } 
        }
    }


