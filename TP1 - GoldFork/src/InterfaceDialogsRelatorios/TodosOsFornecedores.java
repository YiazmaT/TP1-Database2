    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int linha = fornecedoresTable.getSelectedRow();
        if(linha == -1) return;
        
        ArrayList<Loja> l= BancoDeDados.recuperarNomeEIDLojas(pai.getIdUsuarioLogado());
        
        ModificarFornecedor m = new ModificarFornecedor(this.pai, true, this.fornecedoresArray.get(linha), this.pai, l);
        m.setLocationRelativeTo(this.pai);
        m.setVisible(true);
        
        this.preencherTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

    public void preencherTabela(){
        this.fornecedoresArray = new ArrayList<Fornecedor>();
        DefaultTableModel dtm = (DefaultTableModel) fornecedoresTable.getModel();
        dtm.setRowCount(0);
        ArrayList<Fornecedor> f;
        String [] linha = new String[4];
        for(Loja lojaAtual: lojas){
            f = BancoDeDados.todosOsFornecedoresDeUmaLoja(lojaAtual.getId_lanchonete());
            if(!f.isEmpty()){
                for(Fornecedor forc : f){
                    linha[0] = forc.getNome();
                    linha[1] = forc.getTelefone();
                    linha[2] = forc.getCnpj();
                    linha[3] = lojaAtual.getNome();
                    dtm.addRow(linha);
                    this.fornecedoresArray.add(forc);
                }
            }
        }
    }

